"use client";

import * as React from "react";

export interface SwitchProps {
  checked?: boolean;
  onCheckedChange?: (checked: boolean) => void;
  disabled?: boolean;
  className?: string;
  id?: string;
}

const Switch = React.forwardRef<HTMLButtonElement, SwitchProps>(
  ({ checked = false, onCheckedChange, disabled = false, className = "", id }, ref) => {
    return (
      <button
        ref={ref}
        id={id}
        role="switch"
        type="button"
        aria-checked={checked}
        disabled={disabled}
        onClick={() => onCheckedChange?.(!checked)}
        className={`peer inline-flex h-5 w-9 shrink-0 cursor-pointer items-center rounded-full border-2 border-transparent shadow-sm transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[#00d4ff] focus-visible:ring-offset-2 focus-visible:ring-offset-black disabled:cursor-not-allowed disabled:opacity-50 ${
          checked
            ? "bg-[#00d4ff] shadow-[0_0_10px_rgba(0,212,255,0.5)]"
            : "bg-[#2a2a2a]"
        } ${className}`}
      >
        <span
          className={`pointer-events-none block h-4 w-4 rounded-full shadow-lg ring-0 transition-transform ${
            checked
              ? "translate-x-4 bg-black"
              : "translate-x-0 bg-[#555555]"
          }`}
        />
      </button>
    );
  }
);
Switch.displayName = "Switch";

export { Switch };
